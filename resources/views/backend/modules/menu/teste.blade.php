<p>Apesar de ser uma entidade privada, o SESI/SENAI sempre teve a sua gestão - contábil, financeira, orçamentária, operacional e patrimonial - controlada e fiscalizada pelo Tribunal de Contas da União, assim como o seu orçamento ratificado pela República.</p>
<p><br></p>
<p>Mesmo não estando sujeito ás regras que regem a contratação pública, o SESI/SENAI adotou, já na década de 90, regulamento de licitações e contratos próprio.</p>
<p><br></p>
<p>Antes mesmo de a Lei de Diretrizes Orçamentárias exigir, a entidade já publicava na internet dados de sua execução orçamentária.</p>
<p><br></p>
<p>Ao disponibilizar agora os dados e as informações abaixo, o SESI/SENAI, assume publicamente, o compromisso de elevar o nível de transparência de sua gestão e de ampliar o conhecimento geral de sua realizações.</p>
